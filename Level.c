#include "Level.h"

#define LEVEL_BU    PD_IDR_IDR0

/**********************************************函数定义***************************************************** 
* 函数名称: void LevelInit(void) 
* 输入参数: void 
* 返回参数: void  
* 功    能:   
* 作    者: by lhb_steven
* 日    期: 2016/9/22
************************************************************************************************************/ 
void LevelInit(void) { 
    PD_DDR_DDR0 = 0;
    PD_CR1_C10 = 0;
    PD_CR2_C20 = 0;
}
/**********************************************函数定义***************************************************** 
* 函数名称: u8 LevelRead(void) 
* 输入参数: void 
* 返回参数: u8  
* 功    能:   
* 作    者: by lhb_steven
* 日    期: 2016/9/22
************************************************************************************************************/ 
u8 LevelRead(void) { 
    if(LEVEL_BU == 0) {
        return 0x01;
    } else {
        return 0x00;
    }
}









